                crScrMtx[aIdx,"fCol_Reb"] <- sum(rebCnt)
                rebCnt <- sapply( curCR ,function(crObj){ sum(crObj$summ$summMtx.reb["evt","phReb"]>0) })
                crScrMtx[aIdx,"phReb_Reb"] <- sum(rebCnt)

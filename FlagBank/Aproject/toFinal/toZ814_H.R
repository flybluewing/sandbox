			cnt.w2 <- 0
			if( aCStep[4]==sum(aCStep[c(2,3,5)]) ) cnt<-cnt+1	cnt.w2<-cnt.w2+1

			cntMtx[idx,"cStep.w1"] <- cnt.w1	;cntMtx[idx,"cStep.w2"] <- cnt.w2

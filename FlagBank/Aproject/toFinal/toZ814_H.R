					if( fCutU.hasPtn(c(    6,11         ),aZoid) ) cnt<-cnt+1
					# <11>
					if( fCutU.hasPtn(c(    6,11         ),aZoid) ) cnt<-cnt+1
					if( fCutU.hasPtn(c(      11,NA,NA,40),aZoid) ) cnt<-cnt+1

					if( 1<sum(aCStep==c(12, 9, 9, 9, 4)) ){	# h-2 cStep 에서 3개 이상 자름
						# score <- score + (sum(aCStep==c(12, 9, 9, 9, 4))-1)
					}

					cnt <- 0
					# if( aCStep[ ]%in%c( ) ) cnt<-cnt+1
					score <- score + ifelse(cnt>1,1,0)

					return( score )
				})	;kIdx<-anaFltCnt(fltCnt,rpt)
